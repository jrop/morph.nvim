local Morph = require 'morph'
local h = Morph.h

local H = {}

--- @generic T
--- @param xs any[]
--- @param init? integer
--- @param key string
local function max_width(xs, key, init)
  local max_width = init or 0
  for _, x in ipairs(xs) do
    local w = #x[key]
    if w > max_width then max_width = w end
  end
  return max_width
end

